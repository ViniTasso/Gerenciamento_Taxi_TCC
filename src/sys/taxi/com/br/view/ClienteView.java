    private void deleteDetailButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteDetailButtonActionPerformed
        int index = masterTable.getSelectedRow();
        sys.taxi.com.br.bean.Cliente c = list.get(masterTable.convertRowIndexToModel(index));
        Collection<sys.taxi.com.br.bean.Enderecocli> es = c.getEnderecocliCollection();
        int[] selected = detailTable.getSelectedRows();
        List<sys.taxi.com.br.bean.Enderecocli> toRemove = new ArrayList<sys.taxi.com.br.bean.Enderecocli>(selected.length);
        for (int idx = 0; idx < selected.length; idx++) {
            selected[idx] = detailTable.convertRowIndexToModel(selected[idx]);
            int count = 0;
            Iterator<sys.taxi.com.br.bean.Enderecocli> iter = es.iterator();
            while (count++ < selected[idx]) {
                iter.next();
            }
            sys.taxi.com.br.bean.Enderecocli e = iter.next();
            toRemove.add(e);
            entityManager.remove(e);
        }
        es.removeAll(toRemove);
        masterTable.clearSelection();
        masterTable.setRowSelectionInterval(index, index);
    }//GEN-LAST:event_deleteDetailButtonActionPerformed
    
    private void newDetailButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_newDetailButtonActionPerformed
        int index = masterTable.getSelectedRow();
        sys.taxi.com.br.bean.Cliente c = list.get(masterTable.convertRowIndexToModel(index));
        
        Collection<sys.taxi.com.br.bean.Enderecocli> es = c.getEnderecocliCollection();
        if (es == null) {
            es = new LinkedList<sys.taxi.com.br.bean.Enderecocli>();
            c.setEnderecocliCollection((List) es);
        }
        sys.taxi.com.br.bean.Enderecocli e = new sys.taxi.com.br.bean.Enderecocli();
        entityManager.persist(e);
        e.setCodcliente(c);
        es.add(e);
        masterTable.clearSelection();
        masterTable.setRowSelectionInterval(index, index);
        int row = es.size() - 1;
        detailTable.setRowSelectionInterval(row, row);
        detailTable.scrollRectToVisible(detailTable.getCellRect(row, 0, true));
        
        DialogClienteEnderecoView cev = new DialogClienteEnderecoView(null, true);
        
        String codClienteV = ""+e.getCodcliente();
        codClienteV = codClienteV.substring((codClienteV.indexOf("=")+1), (codClienteV.indexOf("]")-1));

        e.setCodigocliente(Integer.parseInt(codClienteV));
        
        
        cev.setRegistroAtual(e);
        cev.setLocationRelativeTo(null);
        cev.setVisible(true);
        
           if (cev.isConfirmaEndereco()){
            saveButton.doClick();
        }else{
            deleteDetailButton.doClick();
        }

        
    }//GEN-LAST:event_newDetailButtonActionPerformed
    

