    private void saveButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveButtonActionPerformed
        try {
            entityManager.getTransaction().commit();
            entityManager.getTransaction().begin();
        } catch (RollbackException rex) {
            rex.printStackTrace();
            entityManager.getTransaction().begin();
            List<sys.taxi.com.br.bean.Cliente> merged = new ArrayList<sys.taxi.com.br.bean.Cliente>(list.size());
            for (sys.taxi.com.br.bean.Cliente c : list) {
                merged.add(entityManager.merge(c));
                JOptionPane.showMessageDialog(null, "Não é possivel excluir o registro, certifique que este cliente nao esta sendo utilizado em outra tabela !");
                refreshButton.doClick();
            }
            list.clear();
            list.addAll(merged);
        }
    }//GEN-LAST:event_saveButtonActionPerformed

