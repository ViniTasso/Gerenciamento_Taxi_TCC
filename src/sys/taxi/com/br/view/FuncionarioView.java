private void saveButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveButtonActionPerformed
        try {
            entityManager.getTransaction().commit();
            entityManager.getTransaction().begin();
        } catch (RollbackException rex) {
            rex.printStackTrace();
            entityManager.getTransaction().begin();
            List<sys.taxi.com.br.bean.Funcionario> merged = new ArrayList<sys.taxi.com.br.bean.Funcionario>(list.size());
            for (sys.taxi.com.br.bean.Funcionario f : list) {
                merged.add(entityManager.merge(f));
            }
            list.clear();
            list.addAll(merged);
            JOptionPane.showMessageDialog(null, "Não é possivel excluir o registro, certifique que este funcionario nao esta sendo utilizado em outra tabela !");
            refreshButton.doClick();
        }finally{
            PainelFuncionario.setSelectedIndex(0);
        }
}//GEN-LAST:event_saveButtonActionPerformed

