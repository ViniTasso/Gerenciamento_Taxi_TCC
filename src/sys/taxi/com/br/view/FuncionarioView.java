private void BOTImprimir2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BOTImprimir2ActionPerformed
query = entityManager.createNamedQuery("Funcionario.findAll");
        List<sys.taxi.com.br.bean.Funcionario> funcionarios = query.getResultList();
    
    
PrinterMatrix printer=new PrinterMatrix();
printer.setOutSize( funcionarios.size() + 6, 70 );
printer.printCharAtCol( 1, 1, 70, "=" );
printer.printTextLinCol( 2, 1, printer.centralizar(70, "Sistema VGCT - Relatorio de Lucro de caixa"));//42

printer.printCharAtCol( 3, 1, 70, "*" );
printer.printTextLinCol( 4, 1, "Nome Funcionario" );
printer.printTextLinCol( 4, 30, "| CPF" );
printer.printTextLinCol( 4, 48, "| Data Nascimento" );
printer.printCharAtCol( 5, 1, 70, "-" );    

int linha = 6;
for (sys.taxi.com.br.bean.Funcionario func : funcionarios){
    printer.printTextLinCol( linha, 1, func.getNomefunc());
    printer.printTextLinCol( linha, 30,"| "+ func.getCpffunc());
    printer.printTextLinCol( linha, 48,"| "+ func.getDatanasfunc());
    printer.printCharAtCol(linha+1, 1, 60, "-");
    
    linha++;
    
}


printer.printCharAtCol( linha, 1, 70, "=" );    
printer.toImageFile("src/sys/taxi/com/all/Images/Imprensao/funcionario.jpg");
    
    
            //areaRelatorio.setText(carregaArquivo("veiculos.txt"));
            //areaRelatorio.moveCaretPosition(0);
            //jLabel1.setIcon(new ImageIcon("src/veiculos.jpg"));
            folha.setIcon(new javax.swing.ImageIcon(getClass().getResource("/sys/taxi/com/all/Images/Imprensao/funcionario.jpg")));
        
    PainelImpresao.setSelectedIndex(1);
}//GEN-LAST:event_BOTImprimir2ActionPerformed

